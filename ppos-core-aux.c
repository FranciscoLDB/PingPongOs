unsigned int contQ = 20;
unsigned int systemTime = 0;
int timeExecution = 0; 
extern unsigned char preemption;

void trat_temporizador (int signum){ 
    systemTime++;   
    if(taskExec->taskU){
        contQ--;
        timeExecution++;
        if(contQ < 1){
            contQ = 20;
            taskExec->timeExec += 20;
            timeExecution = 0;
            queue_append((queue_t **) &readyQueue, (queue_t *) taskExec);
            task_switch(taskDisp);
        }
   }
}




